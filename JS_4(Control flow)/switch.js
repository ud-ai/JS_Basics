const month="april"
switch(month){
    case "jan":
        console.log("January")
        break;
    case "feb":
        console.log("January")
        break;
    case "march":
        console.log("February")
        break;
    case "april":
        console.log("March")
        break;
    case 5:
        console.log("April")
        break;
    case 6:
        console.log("May")
        break;

        default:
            console.log("default case");
            break;
}